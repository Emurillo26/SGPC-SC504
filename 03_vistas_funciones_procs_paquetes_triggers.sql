-- Trigger 2: fecha de envio automatica al insertar una notificacion
CREATE OR REPLACE TRIGGER FIDE_NOTIFICACION_FECHA_TRG
BEFORE INSERT ON FIDE_NOTIFICACION_TB
FOR EACH ROW
BEGIN
    IF :NEW.FECHA_ENVIO IS NULL THEN
        :NEW.FECHA_ENVIO := SYSDATE;
    END IF;
END;
/

-- Trigger 3: registrar fecha de solicitud automatica si no viene informada
CREATE OR REPLACE TRIGGER FIDE_SOLICITUD_FECHA_TRG
BEFORE INSERT ON FIDE_SOLICITUD_TB
FOR EACH ROW
BEGIN
    IF :NEW.FECHA_SOLICITUD IS NULL THEN
        :NEW.FECHA_SOLICITUD := SYSDATE;
    END IF;
END;
/

-- Trigger 4: al actualizar el estado de una solicitud a Aprobado/Rechazado,
-- completar automaticamente la fecha de respuesta
CREATE OR REPLACE TRIGGER FIDE_SOLICITUD_RESPUESTA_TRG
BEFORE UPDATE OF ID_ESTADO ON FIDE_SOLICITUD_TB
FOR EACH ROW
DECLARE
    V_NOMBRE_ESTADO VARCHAR2(50);
BEGIN
    SELECT NOMBRE INTO V_NOMBRE_ESTADO FROM FIDE_ESTADO_TB WHERE ID_ESTADO = :NEW.ID_ESTADO;
    IF V_NOMBRE_ESTADO IN ('Aprobado', 'Rechazado') AND :NEW.FECHA_RESPUESTA IS NULL THEN
        :NEW.FECHA_RESPUESTA := SYSDATE;
    END IF;
END;
/

-- Trigger 5: al registrar una revision de solicitud, sincronizar el estado
-- de la solicitud correspondiente
CREATE OR REPLACE TRIGGER FIDE_REVISION_SINCRONIZA_TRG
AFTER INSERT ON FIDE_REVISION_SOLICITUD_TB
FOR EACH ROW
BEGIN
    UPDATE FIDE_SOLICITUD_TB
    SET ID_ESTADO = :NEW.ID_ESTADO,
        FECHA_RESPUESTA = NVL(FECHA_RESPUESTA, SYSDATE)
    WHERE ID_SOLICITUD = :NEW.ID_SOLICITUD;
END;
/
