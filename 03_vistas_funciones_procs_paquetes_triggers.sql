-- Vista 1: Programas activos con datos del curso
CREATE OR REPLACE VIEW FIDE_PROGRAMAS_ACTIVOS_V AS
SELECT p.ID_PROGRAMA,
       c.CODIGO_CURSO,
       c.NOMBRE_CURSO,
       c.CREDITOS,
       p.FECHA_CREACION,
       e.NOMBRE AS ESTADO
FROM   FIDE_PROGRAMA_TB p
JOIN   FIDE_CURSO_TB c ON p.ID_CURSO = c.ID_CURSO
JOIN   FIDE_ESTADO_TB e ON p.ID_ESTADO = e.ID_ESTADO
WHERE  e.NOMBRE = 'Activo';

-- Vista 2: Cursos agrupados por carrera
CREATE OR REPLACE VIEW FIDE_CURSOS_POR_CARRERA_V AS
SELECT ca.NOMBRE AS CARRERA,
       cu.CODIGO_CURSO,
       cu.NOMBRE_CURSO,
       cu.CREDITOS,
       cc.TIPO,
       cc.CICLO
FROM   FIDE_CURSO_CARRERA_TB cc
JOIN   FIDE_CARRERA_TB ca ON cc.ID_CARRERA = ca.ID_CARRERA
JOIN   FIDE_CURSO_TB cu ON cc.ID_CURSO = cu.ID_CURSO
ORDER BY ca.NOMBRE, cc.CICLO;

-- Vista 3: Solicitudes pendientes con datos del solicitante
CREATE OR REPLACE VIEW FIDE_SOLICITUDES_PENDIENTES_V AS
SELECT s.ID_SOLICITUD,
       u.NOMBRE || ' ' || u.APELLIDO_1 AS SOLICITANTE,
       ts.NOMBRE AS TIPO_SOLICITUD,
       s.JUSTIFICACION,
       s.FECHA_SOLICITUD
FROM   FIDE_SOLICITUD_TB s
JOIN   FIDE_USUARIO_TB u ON s.CEDULA = u.CEDULA
JOIN   FIDE_TIPO_SOLICITUD_TB ts ON s.ID_TIPO_SOLICITUD = ts.ID_TIPO_SOLICITUD
JOIN   FIDE_ESTADO_TB e ON s.ID_ESTADO = e.ID_ESTADO
WHERE  e.NOMBRE = 'Pendiente';

-- Vista 4: Solicitudes ya resueltas (aprobadas o rechazadas)
CREATE OR REPLACE VIEW FIDE_SOLICITUDES_RESUELTAS_V AS
SELECT s.ID_SOLICITUD,
       u.NOMBRE || ' ' || u.APELLIDO_1 AS SOLICITANTE,
       e.NOMBRE AS ESTADO,
       s.FECHA_SOLICITUD,
       s.FECHA_RESPUESTA
FROM   FIDE_SOLICITUD_TB s
JOIN   FIDE_USUARIO_TB u ON s.CEDULA = u.CEDULA
JOIN   FIDE_ESTADO_TB e ON s.ID_ESTADO = e.ID_ESTADO
WHERE  e.NOMBRE IN ('Aprobado', 'Rechazado')
ORDER BY s.FECHA_RESPUESTA DESC;

-- Vista 5: Version vigente de cada programa (la de mayor numero)
CREATE OR REPLACE VIEW FIDE_VERSIONES_VIGENTES_V AS
SELECT vp.ID_PROGRAMA,
       vp.ID_VERSION,
       vp.NUMERO_VERSION,
       vp.FECHA_PUBLICACION,
       m.NOMBRE AS MODALIDAD
FROM   FIDE_VERSION_PROGRAMA_TB vp
JOIN   FIDE_MODALIDAD_TB m ON vp.ID_MODALIDAD = m.ID_MODALIDAD
WHERE  vp.NUMERO_VERSION = (
           SELECT MAX(v2.NUMERO_VERSION)
           FROM   FIDE_VERSION_PROGRAMA_TB v2
           WHERE  v2.ID_PROGRAMA = vp.ID_PROGRAMA);

-- Vista 6: Notificaciones por usuario destinatario
CREATE OR REPLACE VIEW FIDE_NOTIFICACIONES_USUARIO_V AS
SELECT nu.CEDULA,
       n.ID_NOTIFICACION,
       n.ASUNTO,
       n.MENSAJE,
       n.FECHA_ENVIO
FROM   FIDE_NOTIFICACION_USUARIO_TB nu
JOIN   FIDE_NOTIFICACION_TB n ON nu.ID_NOTIFICACION = n.ID_NOTIFICACION
ORDER BY n.FECHA_ENVIO DESC;

-- Vista 7: Usuarios agrupados por rol asignado
CREATE OR REPLACE VIEW FIDE_USUARIOS_POR_ROL_V AS
SELECT r.NOMBRE AS ROL,
       u.CEDULA,
       u.NOMBRE || ' ' || u.APELLIDO_1 || ' ' || u.APELLIDO_2 AS NOMBRE_COMPLETO
FROM   FIDE_USUARIO_ROL_TB ur
JOIN   FIDE_ROL_TB r ON ur.ID_ROL = r.ID_ROL
JOIN   FIDE_USUARIO_TB u ON ur.CEDULA = u.CEDULA
ORDER BY r.NOMBRE, u.APELLIDO_1;

-- Vista 8: Revisiones hechas sobre cada solicitud
CREATE OR REPLACE VIEW FIDE_REVISIONES_SOLICITUD_V AS
SELECT rs.ID_SOLICITUD,
       u.NOMBRE || ' ' || u.APELLIDO_1 AS REVISOR,
       e.NOMBRE AS RESULTADO,
       rs.COMENTARIO,
       rs.FECHA_REVISION
FROM   FIDE_REVISION_SOLICITUD_TB rs
JOIN   FIDE_USUARIO_TB u ON rs.CEDULA = u.CEDULA
JOIN   FIDE_ESTADO_TB e ON rs.ID_ESTADO = e.ID_ESTADO
ORDER BY rs.FECHA_REVISION DESC;

-- Vista 9: Bibliografia asignada a cada version de programa
CREATE OR REPLACE VIEW FIDE_BIBLIOGRAFIA_POR_VERSION_V AS
SELECT vb.ID_VERSION,
       b.TITULO,
       b.AUTOR,
       b.ANIO_PUBLICACION
FROM   FIDE_VERSION_BIBLIOGRAFIA_TB vb
JOIN   FIDE_BIBLIOGRAFIA_TB b ON vb.ID_BIBLIOGRAFIA = b.ID_BIBLIOGRAFIA
ORDER BY vb.ID_VERSION;

-- Vista 10: Ciclos academicos con su periodo de cambio asociado
CREATE OR REPLACE VIEW FIDE_CICLOS_PERIODOS_V AS
SELECT ca.ID_CICLO,
       ca.ANIO,
       ca.CICLO,
       ca.FECHA_INICIO AS INICIO_CICLO,
       ca.FECHA_FIN AS FIN_CICLO,
       pc.FECHA_INICIO AS INICIO_PERIODO_CAMBIO,
       pc.FECHA_FIN AS FIN_PERIODO_CAMBIO
FROM   FIDE_CICLO_ACADEMICO_TB ca
LEFT JOIN FIDE_PERIODO_CAMBIO_TB pc ON ca.ID_CICLO = pc.ID_CICLO
ORDER BY ca.ANIO DESC, ca.CICLO;



