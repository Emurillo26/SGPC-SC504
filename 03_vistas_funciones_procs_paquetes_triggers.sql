-- Trigger 1: fecha de registro automatica al insertar un usuario
CREATE OR REPLACE TRIGGER FIDE_USUARIO_FECHA_TRG
BEFORE INSERT ON FIDE_USUARIO_TB
FOR EACH ROW
BEGIN
    IF :NEW.FECHA_REGISTRO IS NULL THEN
        :NEW.FECHA_REGISTRO := SYSDATE;
    END IF;
END;
