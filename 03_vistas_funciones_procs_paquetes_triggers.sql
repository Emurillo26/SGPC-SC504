-- Funcion 8: numero de la version vigente de un programa
CREATE OR REPLACE FUNCTION FIDE_PROGRAMA_VERSION_VIGENTE_FN(P_ID_PROGRAMA IN NUMBER)
RETURN NUMBER IS
    V_VERSION NUMBER;
BEGIN
    SELECT MAX(NUMERO_VERSION) INTO V_VERSION
    FROM   FIDE_VERSION_PROGRAMA_TB
    WHERE  ID_PROGRAMA = P_ID_PROGRAMA;
    RETURN NVL(V_VERSION, 0);
END;
/

-- Funcion 9: total de solicitudes pendientes de un docente
CREATE OR REPLACE FUNCTION FIDE_DOCENTE_SOLICITUDES_PENDIENTES_FN(P_CEDULA IN VARCHAR2)
RETURN NUMBER IS
    V_TOTAL NUMBER;
BEGIN
    SELECT COUNT(*) INTO V_TOTAL
    FROM   FIDE_SOLICITUD_TB s
    JOIN   FIDE_ESTADO_TB e ON s.ID_ESTADO = e.ID_ESTADO
    WHERE  s.CEDULA = P_CEDULA
    AND    e.NOMBRE = 'Pendiente';
    RETURN V_TOTAL;
END;
/

-- Funcion 10: verificar si un ciclo academico esta activo
CREATE OR REPLACE FUNCTION FIDE_CICLO_ACTIVO_FN(P_ID_CICLO IN NUMBER)
RETURN CHAR IS
    V_NOMBRE VARCHAR2(50);
BEGIN
    SELECT e.NOMBRE INTO V_NOMBRE
    FROM   FIDE_CICLO_ACADEMICO_TB c
    JOIN   FIDE_ESTADO_TB e ON c.ID_ESTADO = e.ID_ESTADO
    WHERE  c.ID_CICLO = P_ID_CICLO;
    IF V_NOMBRE = 'Activo' THEN RETURN 'S'; ELSE RETURN 'N'; END IF;
EXCEPTION WHEN NO_DATA_FOUND THEN RETURN 'N';
END;
/

-- Funcion 11: obtener el nombre de un estado por su id
CREATE OR REPLACE FUNCTION FIDE_ESTADO_NOMBRE_FN(P_ID_ESTADO IN NUMBER)
RETURN VARCHAR2 IS
    V_NOMBRE VARCHAR2(50);
BEGIN
    SELECT NOMBRE INTO V_NOMBRE FROM FIDE_ESTADO_TB WHERE ID_ESTADO = P_ID_ESTADO;
    RETURN V_NOMBRE;
EXCEPTION WHEN NO_DATA_FOUND THEN RETURN 'DESCONOCIDO';
END;
/

-- Funcion 12: verificar si un usuario tiene un rol especifico
CREATE OR REPLACE FUNCTION FIDE_USUARIO_TIENE_ROL_FN(P_CEDULA IN VARCHAR2, P_ID_ROL IN NUMBER)
RETURN CHAR IS
    V_TOTAL NUMBER;
BEGIN
    SELECT COUNT(*) INTO V_TOTAL FROM FIDE_USUARIO_ROL_TB
    WHERE CEDULA = P_CEDULA AND ID_ROL = P_ID_ROL;
    IF V_TOTAL > 0 THEN RETURN 'S'; ELSE RETURN 'N'; END IF;
END;
/

-- Funcion 13: verificar si un periodo de cambio esta activo (estado)
CREATE OR REPLACE FUNCTION FIDE_PERIODO_ACTIVO_FN(P_ID_PERIODO IN NUMBER)
RETURN CHAR IS
    V_NOMBRE VARCHAR2(50);
BEGIN
    SELECT e.NOMBRE INTO V_NOMBRE
    FROM   FIDE_PERIODO_CAMBIO_TB p
    JOIN   FIDE_ESTADO_TB e ON p.ID_ESTADO = e.ID_ESTADO
    WHERE  p.ID_PERIODO = P_ID_PERIODO;
    IF V_NOMBRE = 'Activo' THEN RETURN 'S'; ELSE RETURN 'N'; END IF;
EXCEPTION WHEN NO_DATA_FOUND THEN RETURN 'N';
END;
/

-- Funcion 14: total de objetivos definidos para una version de programa
CREATE OR REPLACE FUNCTION FIDE_VERSION_TOTAL_OBJETIVOS_FN(P_ID_VERSION IN NUMBER)
RETURN NUMBER IS
    V_TOTAL NUMBER;
BEGIN
    SELECT COUNT(*) INTO V_TOTAL FROM FIDE_OBJETIVO_TB WHERE ID_VERSION = P_ID_VERSION;
    RETURN V_TOTAL;
END;
/

-- Funcion 15: obtener el primer correo registrado de un usuario
CREATE OR REPLACE FUNCTION FIDE_USUARIO_CORREO_PRINCIPAL_FN(P_CEDULA IN VARCHAR2)
RETURN VARCHAR2 IS
    V_CORREO VARCHAR2(100);
BEGIN
    SELECT MIN(CORREO) INTO V_CORREO FROM FIDE_CORREO_TB WHERE CEDULA = P_CEDULA;
    RETURN V_CORREO;
EXCEPTION WHEN NO_DATA_FOUND THEN RETURN NULL;
END;
/



